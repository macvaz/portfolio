from datetime import date


def _generate_chart_series(
    start_year: int = 2010,
    months: int = 198,
    portfolio_drift: float = 0.0035,
    benchmark_drift: float = 0.0042,
) -> dict:
    labels: list[str] = []
    portfolio: list[float] = []
    benchmark: list[float] = []

    portfolio_value = 100.0
    benchmark_value = 100.0
    year = start_year
    month = 1

    for _ in range(months):
        labels.append(f"{year:04d}-{month:02d}")
        portfolio_value *= 1 + portfolio_drift
        benchmark_value *= 1 + benchmark_drift
        portfolio.append(round(portfolio_value, 2))
        benchmark.append(round(benchmark_value, 2))
        month += 1
        if month > 12:
            month = 1
            year += 1

    return {"labels": labels, "portfolio": portfolio, "benchmark": benchmark}


